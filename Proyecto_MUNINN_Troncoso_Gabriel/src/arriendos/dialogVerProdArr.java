    private void btIngresarProductoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btIngresarProductoActionPerformed
        if(!txtCantidadProdDev.getText().equals(""))
        {
                    int horasTotales = Integer.parseInt(lbHorasArr.getText());
        
                    Object []filanew= new Object[6];
                    String id = lbCodProdSelec.getText(); //id producto
                    String descp= txtaProductoDescrp.getText(); //nombre producto
                    String valorA = lbValorArr.getText(); //arriendo unit
                    String valorGarant = lbValorGarant.getText(); //garantia unit                  
                    
                    int ValorGarantia = Integer.parseInt(valorGarant);
                    int ValorArrHr = Integer.parseInt(valorA);
                    
                    int cInic = Integer.parseInt(lbCantidadEntregada.getText());//cantidad inicial
                    System.out.print("Cantidad Entregada: "+cInic+"\n");
                    
                    int cFin = Integer.parseInt(txtCantidadProdDev.getText());// cantidad entreg
                    System.out.print("Total Devuelta: "+cFin+"\n");
                    
                    //valor garanti por numero resultante a resta entre cantidad entrgada y cantidad dev
                    
                    int resultadoRest = cInic-cFin;
                    int garantia = resultadoRest*ValorGarantia;
                    int TotalArrxProd = ValorArrHr*cInic;
                    int totarriendos1 = TotalArrxProd*horasTotales; //total arriendo mas suma de horas
                    
                    System.out.print("Total Arr 000: "+cFin+"\n");
                    
                    
                    System.out.print("Total Arr 000: "+cFin+"\n");
                    System.out.print("Total Arr 000: "+TotalArrxProd+"\n");
                    System.out.print("Total Arr 001: "+totarriendos1+"\n");
                    
                    if(cInic>=cFin)
                    {
                        
                        filanew[0]=id;
                        filanew[1]=descp;
                        filanew[2]=cFin; 
                        filanew[3]=TotalArrxProd; 
                        filanew[4]=garantia;
                        filanew[5]=totarriendos1; 
                        
                        System.out.print("Cantidad Actulizada ingresada: "+cFin+"\n"); 
                        
                        tablaProductoFinArr.addRow(filanew); 
                    
                    JOptionPane.showMessageDialog(rootPane, "Producto (ID "+id+") Verificado Correctamente","Notificaci√≥n", JOptionPane.PLAIN_MESSAGE, iconoInfo);
                    this.dispose();
                    
                    }else
                    {
                        JOptionPane.showMessageDialog(rootPane, "Error: Ud ingreso "+cFin+" producto(s) devueltos, \n"
                                + "Pero solo se entregaron "+cInic+" producto(s)","Notificaci√≥n", JOptionPane.PLAIN_MESSAGE, iconoError);
                        txtCantidadProdDev.setText("");
                        txtCantidadProdDev.requestFocus();
                    }
                       
        }
        else
        {
        JOptionPane.showMessageDialog(rootPane, "Ingrese Cantidad del Producto devuelto","Notificaci√≥n", JOptionPane.PLAIN_MESSAGE, iconoAlerta);
        }
    }//GEN-LAST:event_btIngresarProductoActionPerformed

