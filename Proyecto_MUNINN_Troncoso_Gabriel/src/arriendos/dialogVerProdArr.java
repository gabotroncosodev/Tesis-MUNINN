public boolean validarCamposNProdl()
    {
    boolean respuesta=true;
    String variable1 = "";
    if(txtCantidadProdDev.getText().equals(""))
                {
                
                variable1=variable1+ "- Debe ingresar una cantidad\n";
                respuesta=false;
                txtCantidadProdDev.requestFocus();
                }
    if(lbCodProdSelec.getText().equals("No ha seleccionado nada"))
                {
                variable1= variable1 + "- Seleccione un Producto\n";
                lbCodProdSelec.requestFocus();
                respuesta=false;
                }

     if(!respuesta)
    {
      JOptionPane.showMessageDialog(rootPane, variable1,"Notificaci√≥n", JOptionPane.PLAIN_MESSAGE, iconoAlerta);
        }
    return respuesta;
    }

