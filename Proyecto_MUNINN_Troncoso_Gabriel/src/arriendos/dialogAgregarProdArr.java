public boolean validarCamposNProdl()
    {
    boolean respuesta=true;
    String variable1 = "";
    if(txtCantidadProd.getText().equals(""))
                {
                
                variable1=variable1+ "Debe ingresar una cantidad\n";
                respuesta=false;
                txtCantidadProd.requestFocus();
                }
    else if(txtCantidadProd.getText().equals("0"))
                {
                
                variable1=variable1+ "Cantidad no Valida\n";
                respuesta=false;
                txtCantidadProd.setText("");
                txtCantidadProd.requestFocus();
                }

     if(!respuesta)
    {
      JOptionPane.showMessageDialog(rootPane, variable1,  "Notificaci√≥n", JOptionPane.PLAIN_MESSAGE, iconoAlerta);
        }
    return respuesta;
    }

