    private void btAddProdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAddProdActionPerformed

        if (VerificarCamposProd())
            {
             
            //foto
            String foto = txtRuta.getText();
            productoclase.setRuta_foto(foto);
        
            //descripcion
            String descrip = textDescrp.getText().toUpperCase();
            productoclase.setDetalle_producto(descrip);
        
            //cantidad bodega
            String cantidad = txtCantidad.getText();
            int cantid= Integer.parseInt(cantidad);
            productoclase.setCantidad_bodega(cantid);
        
            //costo unitario
            String costunitA = txtPUnitArr.getText();        
            int costUnA= Integer.parseInt(costunitA);
            productoclase.setCosto_unit_arr_producto(costUnA);
        
            //costo producto
            String costunitC = txtCostUnitC.getText();   
            int costunit= Integer.parseInt(costunitC);
            productoclase.setCosto_Producto(costunit);       
        
            //marca producto
            String marcProd = txtMarcaProd.getText().toUpperCase();
            productoclase.setMarca_producto(marcProd);
        
            //lista categoria
            String nomCategoria =listCategorias.getSelectedValue().toString();
            int idc = sqlproducto.getIdCategoria(nomCategoria);
            productoclase.setId_Categoria(idc);
            
            String nomProveedor =listProveedor.getSelectedValue().toString();
            int idp = sqlproducto.getIdProveedor(nomProveedor);
            proveedorclase.setId_proveedor(idp);
                    
            String existe = sqlproducto.existeProducto(marcProd, descrip , costUnA, costunit);
                if (existe.equals(""))
                    {
                   //sqlproducto.agregarProducto(productoclase, proveedorclase);
                   
                   if(sqlproducto.agregarProducto(productoclase,proveedorclase))
                   {
                   JOptionPane.showMessageDialog(rootPane, "Producto agregado correctamente",  "Notificación", JOptionPane.PLAIN_MESSAGE, iconoBueno);
                   this.dispose();
                   }
                 
                }
                else
                {
                JOptionPane.showMessageDialog(rootPane, "Atención, el Producto ya esta Registrado",  "Notificación", JOptionPane.PLAIN_MESSAGE, iconoError);
                }
                 
                
            }

        
        
    }//GEN-LAST:event_btAddProdActionPerformed

