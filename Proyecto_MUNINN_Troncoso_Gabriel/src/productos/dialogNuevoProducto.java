public boolean VerificarCamposProd ()
{
    
    boolean respuesta=true;
    
    String variable1 = "";
    if(txtMarcaProd.getText().equals(""))
                {
                
                variable1=variable1+ "Debe ingresar una Marca \n";
                respuesta=false;
                txtMarcaProd.requestFocus();
                }
    else if(textDescrp.getText().equals(""))
                {
                
                variable1=variable1+ "Debe ingresar una Descripción \n";
                respuesta=false;
                textDescrp.requestFocus();
                }
     else if(txtCantidad.getText().equals(""))
                {
                
                variable1=variable1+ "Debe ingresar una Cantidad \n";
                respuesta=false;
                txtCantidad.requestFocus();
                }
      else if(txtPUnitArr.getText().equals(""))
                {
                
                variable1=variable1+ "Debe ingresar la cantidad de Precio Unitario \n";
                respuesta=false;
                txtPUnitArr.requestFocus();
                }
       else if(txtCostUnitC.getText().equals(""))
                {
                
                variable1=variable1+ "Debe ingresar un Costo Unitario \n";
                respuesta=false;
                txtCostUnitC.requestFocus();
                }
        else if(txtArchivo.getText().equals(""))
                {
                
                variable1=variable1+"Debe ingresar una imagen \n";
                respuesta=false;
                txtArchivo.requestFocus();
                }
        else if (listProveedor.getSelectedIndex()==-1)
        {
            variable1=variable1+"Debe Seleccionar un Proveedor \n";
            respuesta=false;
        }    
        else if (listCategorias.getSelectedIndex()==-1)
        {
            variable1=variable1+"Debe Seleccionar una Categoria \n";
            respuesta=false;
        }
        
       if(!respuesta)
    {
      JOptionPane.showMessageDialog(rootPane, variable1,  "Notificación", JOptionPane.PLAIN_MESSAGE, iconoAlerta);
        }
    
    return respuesta;
    
}
    
